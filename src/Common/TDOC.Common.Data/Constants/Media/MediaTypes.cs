namespace TDOC.Common.Data.Constants.Media
{
    /// <summary>
    /// Contains definitions of media (MIME) types and methods for converting
    /// from different media formats and identifiers to media types.
    /// </summary>
    public class MediaTypes
    {
        // Audio media types

        /// <summary>
        /// Audio - waveform.
        /// </summary>
        public const string AudioWave = "audio/wav";

        // Image media types

        /// <summary>
        /// Image - bitmap.
        /// </summary>
        public const string ImageBitmap = "image/bmp";

        /// <summary>
        /// Image - GIF (Graphics Interchange Format).
        /// </summary>
        public const string ImageGif = "image/gif";

        /// <summary>
        /// Image - JPEG (Joint Photographic Experts Group).
        /// </summary>
        public const string ImageJpeg = "image/jpeg";

        /// <summary>
        /// Image - PNG (Portable Network Graphics).
        /// </summary>
        public const string ImagePng = "image/png";

        // Text media types

        /// <summary>
        /// Text - plain.
        /// </summary>
        public const string TextPlain = "text/plain";

        // Video media types

        /// <summary>
        /// Video - AVI (Audio Video Interleave).
        /// </summary>
        public const string VideoAVInterleave = "video/x-msvideo";

        /// <summary>
        /// Video - MP4 (MPEG-4 Part 14).
        /// </summary>
        public const string VideoMP4 = "video/mp4";

        /// <summary>
        /// Video - Ogg.
        /// </summary>
        public const string VideoOgg = "video/ogg";

        /// <summary>
        /// Video - Quicktime.
        /// </summary>
        public const string VideoQuicktime = "video/quicktime";

        /// <summary>
        /// Video - WebM.
        /// </summary>
        public const string VideoWebM = "video/webm";

        /// <summary>
        /// Video - WMV (Windows Media Video).
        /// </summary>
        public const string VideoWindowsMedia = "video/x-ms-wmv";
    }
}