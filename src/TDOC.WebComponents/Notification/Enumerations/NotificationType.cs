namespace TDOC.WebComponents.Notification.Enumerations;

public enum NotificationType
{
    /// <summary>
    /// System type is displaying in main header panel.
    /// </summary>
    System = 0,

    /// <summary>
    /// Workflow type is displaying in workflow header panel.
    /// </summary>
    Workflow = 1,

    /// <summary>
    /// Instant type is reserved for toast panel.
    /// </summary>
    Instant = 2 
}